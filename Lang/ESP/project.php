                    <h3 class="descparag1-1">CYBERNETIC ¿ QUÉ ES ?</h3>
                    <p class="descparag1">
                        En asociación con la Gendarmería Nacional, el proyecto CyberNeTic tiene como objetivo realizar una investigación de acción en el corazón de las ciencias de la información y la comunicación para estudiar formas de expresión de violencia utilizando nuevas tecnologías, especialmente el ciberacoso. . Se trata de analizar los discursos de experiencias (víctimas, investigadores) y los intercambios de conversaciones (sms, redes sociales, correos, etc.), para identificar los fenómenos de engranaje, las estrategias de influencia y Manipulado por los depredadores. Al identificar los comportamientos específicos asociados con el ciberespacio, este enfoque innovador para la investigación aplicada reforzará concretamente los medios de investigación e intervención de los gendarmes internamente, mediante el desarrollo de un guión para mejorar sus prácticas profesionales en este caso. delito, pero también prevención externamente, mediante la creación de una aplicación móvil para el público en general.
                    </p>
                    <h3>OBJETIVOS DEL PROYECTO</h3>
                    <p>
                        <b>Desarrollar una dinámica de investigación emergente</b> que se basa en una recopilación de datos cualitativos sin precedentes en Francia que aún no se ha explotado como parte del trabajo científico en humanidades y ciencias sociales. <br>

                        <b>Realizar una investigación de acción</b> con un fuerte impacto social que tiene como objetivo mejorar las prácticas profesionales de los investigadores de gendarmería a través de un enfoque integral de las ciberviolencias entre los jóvenes (de 10 a 25 años de edad) y reforzar sus medios de intervención en el campo, especialmente en el entorno educativo, en luchando contra el ciberacoso creando una aplicación móvil. <br>

                        <b>Abrir y consolidar un diálogo científico</b> entre la Universidad Bordeaux Montaigne y la institución de la Gendarmería Nacional para desarrollar una dinámica científica renovada en las Ciencias de la Información y la Comunicación, en línea con la investigación contigua llevada a cabo por el laboratorio MICA. <br>
                    </p>
