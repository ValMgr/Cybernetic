                    <h3 class="descparag1-1">CYBERNETIC WHAT IS IT ?</h3>
                    <p class="descparag1">
                        In partnership with the National Gendarmerie, the CyberNeTic project aims to conduct an action-research at the heart of information and communication sciences to study forms of expression of violence using new technologies, particularly cyberbullying. . It is a question of analyzing the speeches of experiences (victims, investigators) and the exchanges of conversation (sms, social networks, mails, etc.), in order to identify the phenomena of gearing, the strategies of influence and manipulated by predators. By identifying the specific behaviors associated with cyberstalking, this innovative approach to applied research will concretely reinforce the means of investigation and intervention of gendarmes internally, by developing a script to improve their professional practices on this case. offense, but also prevention externally, by creating a mobile application for the general public.
                    </p>
                    <h3>THE PROJECT'S OBJECTIVES</h3>
                    <p>
                        <b>Develop an emerging research dynamic</b> which is based on an unprecedented qualitative data collection in France never yet exploited as part of scientific work in the humanities and social sciences. <br>

                        <b>Conduct an action research</b> with a strong social impact that aims to improve the professional practices of gendarmerie investigators through a comprehensive approach to cyberviolences among young people (aged 10-25) and to reinforce their means of intervention in the field, especially in the educational environment, in fighting cyberbullying by creating a mobile app. <br>

                        <b>Open and consolidate a scientific dialogue</b> between the University Bordeaux Montaigne and the institution of the National Gendarmerie in order to develop a renewed scientific dynamic in Information and Communication Sciences which is in line with the contiguous research carried out by the MICA laboratory. <br>
                    </p>
